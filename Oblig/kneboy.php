<h2 class="text-center">Knebøy</h2>

<p>Knebøy er en styrkeøvelsen for beina hvor du bøyer hoftene og knærne helt til hofteleddet er parallelt med kneleddet eller lavere (lav knebøy). I styrkeløft gjør man dette med en vektstang på ryggen.</p>

<h3>Hvorfor knebøy?</h3>

<p>Knebøy er kanskje den beste øvelsen for å trene styrke i lårene. Forsiden av lårene får virkelig gjennomgå i denne øvelsen. Som i markløft, engasjeres hele kroppen i denne øvelsen. Baken, ryggen, hofter og annen støttemuskulatur blir belastet i denne øvelsen.</p>

<h3>Frontbøy</h3>

<p>Frontbøy er en populær variasjon av knebøy, hvor stangen plasseres på brystet. Man bruker ofte mindre vekt. Denne øvelsen aktiverer lår i større grad, men legger mindre belastning på korsryggen og hofter. Dette kan være en god variant for de som sliter med dårlig rygg.</p>

<h3>Hvordan skal man løfte?</h3>

<p>Plasser vektstangen nedenfor nakken, ikke på selve nakkevirvelen. Beinstilling avhenger av lengden på lårene og mobilitet. Hvis du har lange lår og relativt dårlig mobilitet, trenger du bredere beinstilling. Pass imidlertid på at kneleddet og føttene peker i samme retning. Pusten skal trekkes før du senker stangen. Pust ut på vei opp eller etter løftet.</p>

<p>For å få et godkjent løft i knebøy, må du senke kroppen slik at hofteleddet står parallelt med kneleddet (eller lavere for dyp knebøy).
Til slutt er det viktig å være oppmerksom på ryggen. I denne øvelsen er det veldig viktig at ryggen holdes rett. Buet rygg kan føre til ryggskader i denne øvelsen.
Se video for videre klargjøring. </p>

