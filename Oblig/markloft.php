<h2 class="text-center">Markløft</h2>

<p> Markløft er en styrkeøvelse som innebærer at du skal løfte en stang fra bakken opp til stående.
Slikt sett ligner den mye på løft du gjør hver eneste dag. Det er en fantastisk styrkeøvelse for hele kroppen.
Markløft er nemlig styrkeøvelsen som trener flest muskelgrupper samtidig!</p>

<h3> Hvordan skal man løfte? </h3>

<p>For det første er det viktig å ha riktig fottøy. Her skal man stå stødig på beina, så joggesko og andre sko med tykke, myke såler er ikke å anbefale.
Man bør bruke enten flate sko eller gå barbeint. For det andre er det viktig å ha et riktig grep. For de fleste er det best å ha et overhåndsgrep på stangen. Dette trener grepstyrken din også. For viderekomne, kan alternert grep være verdt å vurdere. Alternert grep innebærer at du tar overhåndsgrep med den ene hånden, og underhåndsgrep med den andre. For det tredje er det viktig å være oppmerksom på pusten. Du bør trekke inn pusten før du begynner løftet.
Pust ut når du har løftet stangen opp. Hva med beinstillingen? Videoene viser vanlig beinstilling, men for noen kan det være bedre med bredere beinstilling.
Dette kalles «sumostilen», da det ligner på hvordan en sumobryter står. </p>

