<h1 align="center">Benkpress</h1>

<p>Benkpress er en god styrkeøvelse for brystet og triceps (bakside armer). Man får også engasjert skuldre og annen støttemuskulatur i overkroppen. Dette er den styrkeøvelsen som utelukkende fokuserer på overkroppen.</p><br/>

<p>Benkpress er en styrkeøvelse for brystet hvor du ligger på en benk med en vektstang plassert på et stativ over deg. Spenn først opp overkroppen (som vist i video lenger ned). Du skal nå løfte stangen fram fra stativet til rett over brystet, og senke den ned mot brystet. Press deretter stangen opp fra brystet.</p> <br/>

<h3>Hvordan skal man løfte?</h3>

<p>Før du kan løfte vektstangen, må du gripe den. Hva slags grep skal du ha? Vel, det avhenger av hva slags muskler du ønsker å fokusere på. Jo bredere grep, jo mer engasjerer du brystmuskulaturen. Jo smalere grep, jo mer engasjerer du triceps. For nybegynnere, vil en mellomting være best. Se video for utdyping. <br/>
Nå som du har grepet stangen, bør du spenne opp kroppen. Du må imidlertid passe deg for å løfte baken fra benken. Baken og skuldre /øvre rygg skal ha god kontakt med benken. Hvor mye svai du kan tillate, avhenger av erfaring og smak. Nybegynnere bør passe seg for å svaie for mye, da de ofte kan ende opp med å løfte baken opp fra benken (som kan føre til skade). </p>

